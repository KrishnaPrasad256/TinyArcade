  bool btn_pressed(int pin, int index) {
    static int lastReading[5] = {1,1,1,1,1};
    static int buttonState[5] = {1,1,1,1,1};      // current stable state
    static int lastButtonState[5] = {1,1,1,1,1};  // previous stable state
    static unsigned long lastDebounceTime[5] = {0,0,0,0,0};

    int reading = digitalRead(pin);

    // Reset debounce timer if input changes
    if (reading != lastReading[index]) {
        lastDebounceTime[index] = millis();
    }

    // If stable for debounce delay -> accept new state
    if ((millis() - lastDebounceTime[index]) > 50) {
        if (reading != buttonState[index]) {
            buttonState[index] = reading;
        }
    }

    lastReading[index] = reading;

    //EDGE DETECTION
    bool pressed = false;

    // Detect HIGH -> LOW (for INPUT_PULLUP)
    if (lastButtonState[index] == HIGH && buttonState[index] == LOW) {
        pressed = true;
    }

    lastButtonState[index] = buttonState[index];

    return pressed;  // true ONLY once per press
}
