void setup(){
  Serial.begin(115200);
  Serial.println("Initialising Tiny_Arcade!");
  //init initial states
  game_state = GAMES_MENU;
  menu_btn = PLAY;
  btn_state = NON;
  games = GAME1;
  //init button pins and pullups
  for (size_t i = 0; i < 5; i++)pinMode(pin[i], INPUT_PULLUP);
  //init intro
  if (!display.begin(SSD1306_SWITCHCAPVCC, OLED_ADDR)) {
    Serial.println("SSD1306 NOT FOUND!");
    while (1); // Stop if OLED not found
  }
  //Draw logo
  drawLogo();
  fadeIn();
  delay(2000);
  //wait for btn press
  displayText(1, 15, 55, "Click any Button");
  for(;;){
    menu_obj.handleInput();
    if(btn_state!=NON) break;
  }
  fadeOut();
  display.clearDisplay();
  display.display();
  

}

void loop(){//main loop
  Serial.print("btn_state: ");
  Serial.println(btn_state);
  Serial.print("game_state: ");
  Serial.println(game_state);
  Serial.print("games: ");
  Serial.println(games);
  Serial.print("menu_btn: ");
  Serial.println(menu_btn);
  Serial.println("=====================");

  bool esc = false;
  switch (game_state)
  {
  case MENU://MENU
    /* Handleinputs and Render the menu UI*/
    while (!esc)
    {
      switch (menu_btn)
      {
      case PLAY://PLAY
        /* what to display when on top of PLAY */
        //initial state.
        //renders the frame1 and then renders the highscore.
        menu_obj.drawMenu_play();
        displayText(2, 7, 51, ""/*put highscore which is converted to string here*/);
        break;
      
      case EXIT://EXIT
        /* what to display when on top of EXIT */
        //renders the frame2 and then renders the highscore.
        menu_obj.drawMenu_exit();
        displayText(2, 7, 51, ""/*put highscore which is converted to string here*/);
        break;
      }

      for(;;){
        menu_obj.handleInput();
        if(btn_state==DOWN){menu_btn=EXIT; break;}
        else if(btn_state==UP){menu_btn=PLAY; break;}
        else if(btn_state==SEL){
          if(menu_btn==EXIT){
            //sets gameState to GAMES_MENU
            game_state=GAMES_MENU;
            menu_btn=PLAY;//resets the menu_btn state.
            esc=true;
            break;
          }else if(menu_btn==PLAY){
            //sets gameState to PLAYING
            game_state=PLAYING;
            esc=true;
            break;
          }
        }
      }
    }
    
    break;
  case GAME_OVER://GAME_OVER    (Only difference in game_over and menu is that a "game over" title is displayed before the game_over menu)
    /* HandleInputs, update game logic and render the next frame */
    while (!esc)
    {
      //"Game Over" - title displaying
      menu_obj.drawGame_over();
      btn_state=NON;
      for(;;){
        menu_obj.handleInput();
        if(btn_state!=NON) break;
      }
      switch (menu_btn)
      {
      case PLAY://PLAY
        /* what to display when on top of PLAY */
        //initial state.
        //renders the frame1 and then renders the highscore.
        menu_obj.drawMenu_play();
        displayText(2, 7, 51, "highscore"/*put highscore which is converted to string here*/);
        break;
      
      case EXIT://EXIT
        /* what to display when on top of EXIT */
        //renders the frame2 and then renders the highscore.
        menu_obj.drawMenu_exit();
        displayText(2, 7, 51, "highscore"/*put highscore which is converted to string here*/);
        break;
      }

      for(;;){
        menu_obj.handleInput();
        if(btn_state==DOWN){menu_btn=EXIT; break;}
        else if(btn_state==UP){menu_btn=PLAY; break;}
        else if(btn_state==SEL){
          if(menu_btn==EXIT){
            //sets gameState to GAMES_MENU
            game_state=GAMES_MENU;
            esc=true;
            break;
          }else if(menu_btn==PLAY){
            //sets gameState to PLAYING
            game_state=PLAYING;
            esc=true;
            break;
          }
        }
      }
    }
    break;
  case PLAYING://PLAYING
    /* according to the game selected, run the specific game */
    switch (games)
    {
    case GAME1://GAME1
      while (!esc)
      {
        /* game logic */
      }
      break;
    
    case GAME2://GAME2
      while (!esc)
      {
        /* game logic */
      }
      break;
    }
    break;
  case GAMES_MENU://GAMES_MENU
    while (!esc)
    {
      //draw the frame for game selection
      //when selected, set game state to playing and also set which game is selected.
      //init initial frame.
      games_menu_obj.drawGame1();
      for(;;){/* This menu selection logic [checking btn state for frame] will only work for 2 Games */
        menu_obj.handleInput();
        if(btn_state==DOWN){games=GAME2;
          games_menu_obj.drawGame2();
          continue;}
        else if(btn_state==UP){games=GAME1;
          games_menu_obj.drawGame1();
          continue;}
        else if(btn_state==SEL){
          //sets gameState to PLAYING
          game_state=MENU;
          esc=true;
          break;}
        }
      }
    
    break;
  }
}
